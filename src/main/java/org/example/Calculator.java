package org.example;

public class Calculator {
    public int add(int fInt, int sInt){
        return fInt+sInt;
    }
    public int sub(int fInt, int sInt){
        return fInt-sInt;
    }
    public int mult(int fInt, int sInt){
        return fInt*sInt;
    }
    public int div(int fInt, int sInt){
        return fInt/sInt;
    }

}
